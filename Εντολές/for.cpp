/*
Σε αυτό το παράδειγμα, ζητάμε από τον χρήστη να εισάγει έναν ακέραιο αριθμό. Στη συνέχεια, 
χρησιμοποιούμε μια δομή επανάληψης for για να εκτυπώσουμε όλους τους αριθμούς από το 1 μέχρι
και τον αριθμό που εισήγαγε ο χρήστης, χρησιμοποιώντας την cout.
*/

#include <iostream>
using namespace std;

int main() {
    int n;
    cout << "Παρακαλώ εισάγετε έναν ακέραιο αριθμό: ";
    cin >> n;

    // Εκτύπωση των αριθμών από το 1 μέχρι το n
    cout << "Οι αριθμοί από το 1 μέχρι το " << n << " είναι: ";
    for (int i = 1; i <= n; i++) {
        cout << i << " "; // Εκτύπωση του αριθμού i
    }
    cout << endl;

    return 0;
}
